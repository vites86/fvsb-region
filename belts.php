         <li>- Підвищення ефективності системи підготовки спортсменів з освоєння технічого арсеналу «бойового багатоборства»;</li>
         <li>- Підвищення результативності виступу спортсменів на змаганнях;</li>
         <li>- Передача спортсменам знань і умінь визнаних Майстрів «бойового багатоборства»;</li>
         <li>- Навчання спортсменів елементам аналізу ефективності технічного арсеналу виду;</li>
         <li>- Визначення ступеня ефективності техніки бойового багатоборства по відношенню до інших стилів єдиноборств;</li>
         <li>- Популяризації «бойового багатоборства».</li>
         </ul>

         <p>Технічна поясова атестація (далі ТПА) проводиться шляхом послідовного присвоєння відповідного кваліфікаційного ступеня за допомогою оцінювання рівня теоретичних знань і практичних умінь, визначених програмою ТПА.</p>
         <p>Рівень підготовленості спортсменів визначається по комплексним екзаменаційнм білетам, розробленим на підставі програми ТПА. Результати здачі </p>атестаційних іспитів, оформляються комісією у вигляді протоколів і представляються Президенту (Голові) для подальшого їх затвердження. За результатами атестування видається наказ по організації, яка проводить атестування.</p>
