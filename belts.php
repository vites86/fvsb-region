        <p>Головною <b>метою</b> технічної поясовой атестації (далі - ТПА) є удосконалення техніки «бойового багатоборства», своєчасне впровадження в стиль прогресивного технічного арсеналу єдиноборств.</p>

        <p>Основними завданнями атестування є:</p>
         <ul>
